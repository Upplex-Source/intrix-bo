<script>
    document.addEventListener( 'DOMContentLoaded', function() {

        let fe = '#{{ $farm_edit }}',
                fileID = '';

        $( fe + '_cancel' ).click( function() {
            window.location.href = '{{ route( 'admin.module_parent.farm.index' ) }}';
        } );

        $( fe + '_submit' ).click( function() {

            resetInputValidation();

            $( 'body' ).loading( {
                message: '{{ __( 'template.loading' ) }}'
            } );

            let formData = new FormData();
            formData.append( 'id', '{{ request( 'id' ) }}' );
            formData.append( 'title', $( fe + '_title' ).val() );
            formData.append( 'owner', $( fe + '_owner' ).val() );
            formData.append( 'address_1', $( fe + '_address_1' ).val() );
            formData.append( 'address_2', $( fe + '_address_2' ).val() );
            formData.append( 'city', $( fe + '_city' ).val() );
            formData.append( 'state', $( fe + '_state' ).val() );
            formData.append( 'postcode', $( fe + '_postcode' ).val() );
            formData.append( 'size', $( fe + '_size' ).val() );
            formData.append( 'phone_number', $( fe + '_phone_number' ).val() );
            formData.append( 'remarks', $( fe + '_remarks' ).val() );
            formData.append( 'galleries', fileID );
            formData.append( '_token', '{{ csrf_token() }}' );

            $.ajax( {
                url: '{{ route( 'admin.farm.updateFarm' ) }}',
                type: 'POST',
                data: formData,
                processData: false,
                contentType:   false,
                success: function( response ) {
                    $( 'body' ).loading( 'stop' );
                    $( '#modal_success .caption-text' ).html( response.message );
                    modalSuccess.toggle();

                    document.getElementById( 'modal_success' ).addEventListener( 'hidden.bs.modal', function (event) {
                        window.location.href = '{{ route( 'admin.module_parent.farm.index' ) }}';
                    } );
                },
                error: function( error ) {
                    $( 'body' ).loading( 'stop' );

                    if ( error.status === 422 ) {
                        let errors = error.responseJSON.errors;
                        $.each( errors, function( key, value ) {
                            $( fe + '_' + key ).addClass( 'is-invalid' ).nextAll( 'div.invalid-feedback' ).text( value );
                        } );
                    } else {
                        $( '#modal_danger .caption-text' ).html( error.responseJSON.message );
                        modalDanger.toggle();
                    }
                }
            } );
        } );

        Dropzone.autoDiscover = false;

        let ownerSelect2 = $( fe + '_owner' ).select2( {
            language: '{{ App::getLocale() }}',
            theme: 'bootstrap-5',
            width: $( this ).data( 'width' ) ? $( this ).data( 'width' ) : $( this ).hasClass( 'w-100' ) ? '100%' : 'style',
            placeholder: $( this ).data( 'placeholder' ),
            closeOnSelect: false,
            ajax: {
                method: 'POST',
                url: '{{ route( 'admin.owner.allOwners' ) }}',
                dataType: 'json',
                delay: 250,
                data: function (params) {
                    return {
                        custom_search: params.term, // search term
                        status: 10,
                        start: ( ( params.page ? params.page : 1 ) - 1 ) * 10,
                        length: 10,
                        _token: '{{ csrf_token() }}',
                    };
                },
                processResults: function (data, params) {
                    params.page = params.page || 1;

                    let processedResult = [];

                    data.owners.map( function( v, i ) {
                        processedResult.push( {
                            id: v.owner.id,
                            text: v.owner.name,
                        } );
                    } );

                    return {
                        results: processedResult,
                        pagination: {
                            more: ( params.page * 10 ) < data.recordsFiltered
                        }
                    };
                }
            },
        } );

        getFarm();

        function getFarm() {

            $( 'body' ).loading( {
                message: '{{ __( 'template.loading' ) }}'
            } );

            $.ajax( {
                url: '{{ route( 'admin.farm.oneFarm' ) }}',
                type: 'POST',
                data: {
                    'id': '{{ request( 'id' ) }}',
                    '_token': '{{ csrf_token() }}'
                },
                success: function( response ) {

                    if ( response.owner.name ) {
                        let option1 = new Option( response.owner.name, response.owner.id, true, true );
                        ownerSelect2.append( option1 );
                        ownerSelect2.trigger( 'change' );
                    }

                    $( fe + '_title' ).val( response.title );
                    $( fe + '_address_1' ).val( response.address_1 );
                    $( fe + '_address_2' ).val( response.address_2 );
                    $( fe + '_postcode' ).val( response.postcode );
                    $( fe + '_state' ).val( response.state );
                    $( fe + '_city' ).val( response.city );
                    $( fe + '_size' ).val( response.size );
                    $( fe + '_phone_number' ).val( response.phone_number );
                    $( fe + '_remarks' ).val( response.remarks );

                    const dropzone = new Dropzone( fe + '_galleries', {
                        url: '{{ route( 'admin.file.upload' ) }}',
                        maxFiles: 10,
                        acceptedFiles: 'image/jpg,image/jpeg,image/png',
                        addRemoveLinks: true,
                        init: function() {

                            let that = this;

                            if ( response.galleries.length != 0 ) {

                                $.each( response.galleries, function( i, v ) {
                                    console.log(v)
                                    if( v.type == 1 ) {
                                        let myDropzone = that,
                                            mockFile = { name: 'Default', size: 1024, accepted: true, id: v.id };

                                        myDropzone.files.push( mockFile );
                                        myDropzone.displayExistingFile( mockFile, v.path );
                                        $( myDropzone.files[myDropzone.files.length - 1].previewElement ).data( 'id', v.id );
                                    }
                                       
                                } );
                            }
                        },
                        removedfile: function( file ) {
                            var idToRemove = file.id;

                            var idArrays = fileID.split(/\s*,\s*/);

                            var indexToRemove = idArrays.indexOf( idToRemove.toString() );
                            if (indexToRemove !== -1) {
                                idArrays.splice( indexToRemove, 1 );
                            }

                            fileID = idArrays.join( ', ' );

                            file.previewElement.remove();

                            removeGallery( idToRemove );

                        },
                        success: function( file, response ) {
                            if ( response.status == 200 )  {
                                if ( fileID !== '' ) {
                                    fileID += ','; // Add a comma if fileID is not empty
                                }
                                fileID += response.data.id;

                                file.previewElement.id = response.data.id;
                            }
                        }
                    } );

                    $( 'body' ).loading( 'stop' );
                },
            } );
        }

        function removeGallery( gallery ) {

            resetInputValidation();

            $( 'body' ).loading( {
                message: '{{ __( 'template.loading' ) }}'
            } );

            let formData = new FormData();
            formData.append( 'id', gallery );
            formData.append( '_token', '{{ csrf_token() }}' );

            $.ajax( {
                url: '{{ route( 'admin.farm.removeFarmGalleryImage' ) }}',
                type: 'POST',
                data: formData,
                processData: false,
                contentType:   false,
                success: function( response ) {
                    $( 'body' ).loading( 'stop' );
                    $( '#modal_success .caption-text' ).html( response.message );
                    modalSuccess.toggle();
                },
                error: function( error ) {
                    $( 'body' ).loading( 'stop' );

                    if ( error.status === 422 ) {
                        let errors = error.responseJSON.errors;
                        $.each( errors, function( key, value ) {
                            $( fe + '_' + key ).addClass( 'is-invalid' ).nextAll( 'div.invalid-feedback' ).text( value );
                        } );
                    } else {
                        $( '#modal_danger .caption-text' ).html( error.responseJSON.message );
                        modalDanger.toggle();
                    }
                }
            } );
        }

    } );
</script>